# Algunas funciones nos entregan un valor para seguir utilizando en nuestro programa. Como la función input(). int() o randint().
# Todas ellas nos devuelven el valor.
# Pero hay otras funciones que simplemente realizan una operación pero no devuelven ningún valor como la función print()
# A estas funciones se les conoce como funciones void y en rigor devuelven el valor None. Que reprsenta un valor nulo

return_value = print("Hola Inmundo")
print(return_value)

other_return_value = int(3.5)
print(other_return_value)

# Las funciones que retornan valores no tienen un nombre especial, solo las void que no tienen valor retorna